st.header("📈 1か月後予想株価について")

st.warning("※「1か月後予想株価」は過去1年間の価格変動トレンド（日次平均リターン）に基づく簡易的な統計予測であり、将来の価格を保証するものではありません。あくまで投資の参考としてご利用ください。")

st.markdown("""
**算出方法:**
1. 過去1年間の日次終値から、日次リターン（前日比変動率）を計算
2. その平均値を「1日あたりの期待リターン」とする
3. 21営業日（約1か月）分を複利計算
4. `予想株価 = 現在株価 × (1 + 平均日次リターン)^21`

📈 はプラス予想（上昇傾向）、📉 はマイナス予想（下降傾向）を示します。
""")
